#include "libft.h"

int	ft_isalpha(int c)
{
	//c가 대문자이거나 소문자이면 1을 출력
	if (!((c >= 65 && c <= 90) || (c >= 97 && c <= 122)))
	{
		return (0);
	}
	return (1);
}
